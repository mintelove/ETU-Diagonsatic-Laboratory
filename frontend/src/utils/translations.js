/**
 * Universal lookup function
 */
export function getTranslation(key, language = 'en', fallback = '') {
  if (!key) return '';
  const lang = language === 'am' ? 'am' : 'en';
  const dict = dictionary[lang] || dictionary.en;

  // Direct key lookup
  if (dict[key]) return dict[key];

  // Trimmed key lookup
  const trimmed = String(key).trim();
  if (dict[trimmed]) return dict[trimmed];

  // Reverse English text match to find Amharic translation
  if (lang === 'am') {
    const entry = Object.entries(dictionary.en).find(
      ([k, v]) => String(v).toLowerCase() === trimmed.toLowerCase()
    );
    if (entry && dictionary.am[entry[0]]) {
      return dictionary.am[entry[0]];
    }
  }

  return fallback || key;
}
